first = 2
second = 10

# basic operators
print(f"{first} + {second} = {first + second}")
print(f"{first} - {second} = {first - second}")
print(f"{first} * {second} = {first * second}")
print(f"{first} / {second} = {first / second}")
print(f"{first} % {second} = {first % second}")

# Python special
print(f"{first} // {second} = {first // second}")
print(f"{first} ** {second} = {first ** second}")
